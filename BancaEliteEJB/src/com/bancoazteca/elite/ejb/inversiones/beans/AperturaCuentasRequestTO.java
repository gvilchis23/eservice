package com.bancoazteca.elite.ejb.inversiones.beans;

public class AperturaCuentasRequestTO extends InversionesRequestTO{
	
	private String producto;
	private String subProducto;
	private AperturaCuentasEntradaTO entrada;
	private String montoAperturaCuenta;
	private String cuentaCargo;
	public String getProducto() {
		return producto;
	}
	public void setProducto(String producto) {
		this.producto = producto;
	}
	public String getSubProducto() {
		return subProducto;
	}
	public void setSubProducto(String subProducto) {
		this.subProducto = subProducto;
	}
	public AperturaCuentasEntradaTO getEntrada() {
		return entrada;
	}
	public void setEntrada(AperturaCuentasEntradaTO entrada) {
		this.entrada = entrada;
	}
	public String getMontoAperturaCuenta() {
		return montoAperturaCuenta;
	}
	public void setMontoAperturaCuenta(String montoAperturaCuenta) {
		this.montoAperturaCuenta = montoAperturaCuenta;
	}
	public String getCuentaCargo() {
		return cuentaCargo;
	}
	public void setCuentaCargo(String cuentaCargo) {
		this.cuentaCargo = cuentaCargo;
	}
}