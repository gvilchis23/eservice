package com.bancoazteca.elite.ejb.inversiones.beans;

public class ResumenOperacionesRequestTO extends InversionesRequestTO {
	
	private String numeroContrato;	
	private String fechaInicioResumen;
	private String fechaFinResumen;
	private String idAlnova;
	private String codigoTransaccion;
	
	public String getNumeroContrato() {
		return numeroContrato;
	}
	public void setNumeroContrato(String numeroContrato) {
		this.numeroContrato = numeroContrato;
	}
	public String getFechaInicioResumen() {
		return fechaInicioResumen;
	}
	public void setFechaInicioResumen(String fechaInicioResumen) {
		this.fechaInicioResumen = fechaInicioResumen;
	}
	public String getFechaFinResumen() {
		return fechaFinResumen;
	}
	public void setFechaFinResumen(String fechaFinResumen) {
		this.fechaFinResumen = fechaFinResumen;
	}
	public String getIdAlnova() {
		return idAlnova;
	}
	public void setIdAlnova(String idAlnova) {
		this.idAlnova = idAlnova;
	}
	public String getCodigoTransaccion() {
		return codigoTransaccion;
	}
	public void setCodigoTransaccion(String codigoTransaccion) {
		this.codigoTransaccion = codigoTransaccion;
	}
	
	
}
