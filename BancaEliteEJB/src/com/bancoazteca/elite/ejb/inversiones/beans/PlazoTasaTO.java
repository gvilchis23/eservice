package com.bancoazteca.elite.ejb.inversiones.beans;

public class PlazoTasaTO {

	private String plazo;
	private String tasa;
	private String tipoPlazo;
	
	public String getPlazo() {
		return plazo;
	}
	public void setPlazo(String plazo) {
		this.plazo = plazo;
	}
	public String getTasa() {
		return tasa;
	}
	public void setTasa(String tasa) {
		this.tasa = tasa;
	}
	public String getTipoPlazo() {
		return tipoPlazo;
	}
	public void setTipoPlazo(String tipoPlazo) {
		this.tipoPlazo = tipoPlazo;
	}
	
	public String getPlazoCombo(){
		return plazo+" "+tipoPlazo;
	}
	
	public String getTasaCombo(){
		return tasa+"%";
	}
}
