package com.bancoazteca.elite.beans;

import java.io.Serializable;

import com.bancoazteca.elite.ejb.dispositivos.util.TipoOperacionTokenEnum;

public class BloqueoFirmaRequestTO implements Serializable {

	private static final long serialVersionUID = -1387958903012190276L;

	private String user;
	private String numeroSerie;
	private String tokenCode;
	private TipoOperacionTokenEnum tipoServicio;
	

	public String getTokenCode() {
		return tokenCode;
	}
	public void setTokenCode(String tokenCode) {
		this.tokenCode = tokenCode;
	}
	public String getUser() {
		return user;
	}
	public void setUser(String user) {
		this.user = user;
	}
	public String getNumeroSerie() {
		return numeroSerie;
	}
	public void setNumeroSerie(String numeroSerie) {
		this.numeroSerie = numeroSerie;
	}
	public TipoOperacionTokenEnum getTipoServicio() {
		return tipoServicio;
	}
	public void setTipoServicio(TipoOperacionTokenEnum tipoServicio) {
		this.tipoServicio = tipoServicio;
	}
	
}