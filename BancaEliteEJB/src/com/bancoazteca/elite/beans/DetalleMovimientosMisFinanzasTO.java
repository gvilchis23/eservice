package com.bancoazteca.elite.beans;

import java.io.Serializable;
import java.util.Collection;

public class DetalleMovimientosMisFinanzasTO implements Serializable{
	
	private static final long serialVersionUID = 1142481330314405336L;
	
	private String comisiones;
	private String cargos;
	private Collection <String> totales;
	private String abonos;
	private String termino;
	private String inicio;
	
	public String getComisiones() {
		return comisiones;
	}
	public void setComisiones(String comisiones) {
		this.comisiones = comisiones;
	}
	public String getCargos() {
		return cargos;
	}
	public void setCargos(String cargos) {
		this.cargos = cargos;
	}
	public Collection<String> getTotales() {
		return totales;
	}
	public void setTotales(Collection<String> totales) {
		this.totales = totales;
	}
	public String getAbonos() {
		return abonos;
	}
	public void setAbonos(String abonos) {
		this.abonos = abonos;
	}
	public String getTermino() {
		return termino;
	}
	public void setTermino(String termino) {
		this.termino = termino;
	}
	public String getInicio() {
		return inicio;
	}
	public void setInicio(String inicio) {
		this.inicio = inicio;
	}

}
