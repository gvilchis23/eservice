package com.bancoazteca.elite.beans;

import java.io.Serializable;

public class TraspasosPropiasRequestTO implements Serializable{
	

	private static final long serialVersionUID = 1573728545179450854L;
	
	private String user;
	private String cuentaOrigenIndex; 
	private String cuentaDestinoIndex; 
	private String importe; 
	private String concepto;
	private String nombreSubmit;
	private String valorSubmit;
	
	public String getUser() {
		return user;
	}
	public void setUser(String user) {
		this.user = user;
	}
	public String getCuentaOrigenIndex() {
		return cuentaOrigenIndex;
	}
	public void setCuentaOrigenIndex(String cuentaOrigenIndex) {
		this.cuentaOrigenIndex = cuentaOrigenIndex;
	}
	public String getCuentaDestinoIndex() {
		return cuentaDestinoIndex;
	}
	public void setCuentaDestinoIndex(String cuentaDestinoIndex) {
		this.cuentaDestinoIndex = cuentaDestinoIndex;
	}
	public String getImporte() {
		return importe;
	}
	public void setImporte(String importe) {
		this.importe = importe;
	}
	public String getConcepto() {
		return concepto;
	}
	public void setConcepto(String concepto) {
		this.concepto = concepto;
	}
	public String getNombreSubmit() {
		return nombreSubmit;
	}
	public void setNombreSubmit(String nombreSubmit) {
		this.nombreSubmit = nombreSubmit;
	}
	public String getValorSubmit() {
		return valorSubmit;
	}
	public void setValorSubmit(String valorSubmit) {
		this.valorSubmit = valorSubmit;
	}
	

}
