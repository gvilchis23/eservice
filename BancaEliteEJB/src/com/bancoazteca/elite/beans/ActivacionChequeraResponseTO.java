package com.bancoazteca.elite.beans;

import java.io.Serializable;
import java.util.Collection;

public class ActivacionChequeraResponseTO implements Serializable {	
	private static final long serialVersionUID = 1L;
	private DispositivoHuellaTO dispositivoHuellaTO;
	private Collection<CuentaTO> cuentas;
	
	private String cuenta;
	private String tipoCuenta;
	private String activar;
	private String chequeInicial;
	private String chequeFinal;
	private String fechaActivacion;
	private boolean consultoChequeras;
	private String descripcion;
	private Collection<TalonarioTO> chequerascuenta;


	public DispositivoHuellaTO getDispositivoHuellaTO() {
		return dispositivoHuellaTO;
	}

	public void setDispositivoHuellaTO(DispositivoHuellaTO dispositivoHuellaTO) {
		this.dispositivoHuellaTO = dispositivoHuellaTO;
	}

	public Collection<CuentaTO> getCuentas() {
		return cuentas;
	}

	public void setCuentas(Collection<CuentaTO> cuentas) {
		this.cuentas = cuentas;
	}

	public String getCuenta() {
		return cuenta;
	}

	public void setCuenta(String cuenta) {
		this.cuenta = cuenta;
	}

	public String getTipoCuenta() {
		return tipoCuenta;
	}

	public void setTipoCuenta(String tipoCuenta) {
		this.tipoCuenta = tipoCuenta;
	}

	public String getActivar() {
		return activar;
	}

	public void setActivar(String activar) {
		this.activar = activar;
	}

	public String getChequeInicial() {
		return chequeInicial;
	}

	public void setChequeInicial(String chequeInicial) {
		this.chequeInicial = chequeInicial;
	}

	public String getChequeFinal() {
		return chequeFinal;
	}

	public void setChequeFinal(String chequeFinal) {
		this.chequeFinal = chequeFinal;
	}

	public String getFechaActivacion() {
		return fechaActivacion;
	}

	public void setFechaActivacion(String fechaActivacion) {
		this.fechaActivacion = fechaActivacion;
	}

	public boolean isConsultoChequeras() {
		return consultoChequeras;
	}

	public void setConsultoChequeras(boolean consultoChequeras) {
		this.consultoChequeras = consultoChequeras;
	}

	public String getDescripcion() {
		return descripcion;
	}

	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}

	public Collection<TalonarioTO> getChequerascuenta() {
		return chequerascuenta;
	}

	public void setChequerascuenta(Collection<TalonarioTO> chequerascuenta) {
		this.chequerascuenta = chequerascuenta;
	}	
}
