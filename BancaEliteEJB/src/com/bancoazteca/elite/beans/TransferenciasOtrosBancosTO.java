package com.bancoazteca.elite.beans;

import java.io.Serializable;

public class TransferenciasOtrosBancosTO implements Serializable{

	private static final long serialVersionUID = 3854796548040448247L;
	
	private String codigoBanco;
	private boolean tef;
	private boolean spei;
	private boolean ambos;
	private String indexTef;
	private String indexSpei;
	
	public String getIndexTef() {
		return indexTef;
	}
	public void setIndexTef(String indexTef) {
		this.indexTef = indexTef;
	}
	public String getIndexSpei() {
		return indexSpei;
	}
	public void setIndexSpei(String indexSpei) {
		this.indexSpei = indexSpei;
	}
	public String getCodigoBanco() {
		return codigoBanco;
	}
	public void setCodigoBanco(String codigoBanco) {
		this.codigoBanco = codigoBanco;
	}
	public boolean isTef() {
		return tef;
	}
	public void setTef(boolean tef) {
		this.tef = tef;
	}
	public boolean isSpei() {
		return spei;
	}
	public void setSpei(boolean spei) {
		this.spei = spei;
	}
	public boolean isAmbos() {
		return ambos;
	}
	public void setAmbos(boolean ambos) {
		this.ambos = ambos;
	}
	

}
