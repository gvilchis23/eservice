package com.bancoazteca.elite.beans;

import java.io.Serializable;

import com.bancoazteca.elite.ejb.inversiones.beans.InversionesRequestTO;

public class ConciliacionRequestTO extends InversionesRequestTO implements Serializable {
	private static final long serialVersionUID = -8462097187122030054L;
	private String folio;
	private String fechaInicial;
	private String fechaFinal;

	public String getFechaInicial() {
		return fechaInicial;
	}

	public void setFechaInicial(String fechaInicial) {
		this.fechaInicial = fechaInicial;
	}

	public String getFechaFinal() {
		return fechaFinal;
	}

	public void setFechaFinal(String fechaFinal) {
		this.fechaFinal = fechaFinal;
	}

	public String getFolio() {
		return folio;
	}

	public void setFolio(String folio) {
		this.folio = folio;
	}
}
