package com.bancoazteca.elite.beans;

import java.io.Serializable;
import java.util.Collection;

public class EstatusCuentaRequestTO implements Serializable {
	private static final long serialVersionUID = 1368438454670562965L;
	
	private String idusuario;
	private Collection<CuentaTO> cuentas;
	private String numero_cuenta;
	private String estatus;
	private String clave;
	private String optionDispositive;
	
	public String getClave() {
		return clave;
	}
	public void setClave(String clave) {
		this.clave = clave;
	}
	public String getOptionDispositive() {
		return optionDispositive;
	}
	public void setOptionDispositive(String optionDispositive) {
		this.optionDispositive = optionDispositive;
	}
	public String getIdusuario() {
		return idusuario;
	}
	public void setIdusuario(String idusuario) {
		this.idusuario = idusuario;
	}
	public Collection<CuentaTO> getCuentas() {
		return cuentas;
	}
	public void setCuentas(Collection<CuentaTO> cuentas) {
		this.cuentas = cuentas;
	}
	public String getNumero_cuenta() {
		return numero_cuenta;
	}
	public void setNumero_cuenta(String numero_cuenta) {
		this.numero_cuenta = numero_cuenta;
	}
	public String getEstatus() {
		return estatus;
	}
	public void setEstatus(String estatus) {
		this.estatus = estatus;
	}
}
