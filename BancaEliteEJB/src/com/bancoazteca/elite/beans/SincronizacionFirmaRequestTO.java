package com.bancoazteca.elite.beans;

import java.io.Serializable;

public class SincronizacionFirmaRequestTO implements Serializable {

	private static final long serialVersionUID = -1387958903012190276L;

	private String user;
	private String numeroSerie;
	private String preToken;
	private String actToken;
	private String tipoValidacion;
	
	public String getTipoValidacion() {
		return tipoValidacion;
	}
	public void setTipoValidacion(String tipoValidacion) {
		this.tipoValidacion = tipoValidacion;
	}
	public String getUser() {
		return user;
	}
	public void setUser(String user) {
		this.user = user;
	}
	public String getNumeroSerie() {
		return numeroSerie;
	}
	public void setNumeroSerie(String numeroSerie) {
		this.numeroSerie = numeroSerie;
	}
	public String getPreToken() {
		return preToken;
	}
	public void setPreToken(String preToken) {
		this.preToken = preToken;
	}
	public String getActToken() {
		return actToken;
	}
	public void setActToken(String actToken) {
		this.actToken = actToken;
	}
}
