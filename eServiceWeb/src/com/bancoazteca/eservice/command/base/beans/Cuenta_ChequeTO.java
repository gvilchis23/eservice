package com.bancoazteca.eservice.command.base.beans;

public class Cuenta_ChequeTO extends Cuenta_CargoTO{
	
	private String clabe;
	private String chequera;
	private String saldo_retenido;
	
	public String getClabe() {
		return clabe;
	}
	public void setClabe(String clabe) {
		this.clabe = clabe;
	}
	public String getChequera() {
		return chequera;
	}
	public void setChequera(String chequera) {
		this.chequera = chequera;
	}
	public String getSaldo_retenido() {
		return saldo_retenido;
	}
	public void setSaldo_retenido(String saldo_retenido) {
		this.saldo_retenido = saldo_retenido;
	}
	
}
