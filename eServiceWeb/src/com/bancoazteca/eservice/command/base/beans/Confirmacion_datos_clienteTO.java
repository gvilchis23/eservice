package com.bancoazteca.eservice.command.base.beans;

import java.util.Collection;

public class Confirmacion_datos_clienteTO {
	
	private String usuario;
	private String correo_electronico;
	private String numero_celular;
	private String compania_celular;
	private Collection<String> coleccion_companias;
	
	public String getUsuario() {
		return usuario;
	}
	public void setUsuario(String usuario) {
		this.usuario = usuario;
	}
	public String getCorreo_electronico() {
		return correo_electronico;
	}
	public void setCorreo_electronico(String correo_electronico) {
		this.correo_electronico = correo_electronico;
	}
	public String getNumero_celular() {
		return numero_celular;
	}
	public void setNumero_celular(String numero_celular) {
		this.numero_celular = numero_celular;
	}
	public String getCompania_celular() {
		return compania_celular;
	}
	public void setCompania_celular(String compania_celular) {
		this.compania_celular = compania_celular;
	}
	public Collection<String> getColeccion_companias() {
		return coleccion_companias;
	}
	public void setColeccion_companias(Collection<String> coleccion_companias) {
		this.coleccion_companias = coleccion_companias;
	}
	
}
