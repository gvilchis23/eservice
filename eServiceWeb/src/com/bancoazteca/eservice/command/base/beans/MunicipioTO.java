package com.bancoazteca.eservice.command.base.beans;

public class MunicipioTO {
	
	private String identificador_municipio;
	private String nombre_municipio;
	private String identificador_entidad_federativa;
	private String nombre_entidad_federativa;
	
	public String getIdentificador_municipio() {
		return identificador_municipio;
	}
	public void setIdentificador_municipio(String identificador_municipio) {
		this.identificador_municipio = identificador_municipio;
	}
	public String getNombre_municipio() {
		return nombre_municipio;
	}
	public void setNombre_municipio(String nombre_municipio) {
		this.nombre_municipio = nombre_municipio;
	}
	public String getIdentificador_entidad_federativa() {
		return identificador_entidad_federativa;
	}
	public void setIdentificador_entidad_federativa(
			String identificador_entidad_federativa) {
		this.identificador_entidad_federativa = identificador_entidad_federativa;
	}
	public String getNombre_entidad_federativa() {
		return nombre_entidad_federativa;
	}
	public void setNombre_entidad_federativa(String nombre_entidad_federativa) {
		this.nombre_entidad_federativa = nombre_entidad_federativa;
	}
}
