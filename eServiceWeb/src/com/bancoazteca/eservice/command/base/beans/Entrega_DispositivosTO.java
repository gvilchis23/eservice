package com.bancoazteca.eservice.command.base.beans;


public class Entrega_DispositivosTO {
	// Direccion de entrega de dispositivos.
	private String direccion;
	private String concepto;
	
	//Personas autoirizadas para recibir los biometricos.
	private String nombre_primera_persona_autorizada;
	private String nombre_segunda_persona_autorizada;
	
	public String getDireccion() {
		return direccion;
	}
	public void setDireccion(String direccion) {
		this.direccion = direccion;
	}
	public String getConcepto() {
		return concepto;
	}
	public void setConcepto(String concepto) {
		this.concepto = concepto;
	}
	public String getNombre_primera_persona_autorizada() {
		return nombre_primera_persona_autorizada;
	}
	public void setNombre_primera_persona_autorizada(
			String nombre_primera_persona_autorizada) {
		this.nombre_primera_persona_autorizada = nombre_primera_persona_autorizada;
	}
	public String getNombre_segunda_persona_autorizada() {
		return nombre_segunda_persona_autorizada;
	}
	public void setNombre_segunda_persona_autorizada(
			String nombre_segunda_persona_autorizada) {
		this.nombre_segunda_persona_autorizada = nombre_segunda_persona_autorizada;
	}
	
	
}
