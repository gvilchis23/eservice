package com.bancoazteca.eservice.command.prueba;

import com.bancoazteca.eservice.command.base.FormBean;

public class PruebaForm extends FormBean {
	
	String numeroCuenta;
	String monto;
	String folio_retencion;
	
	public String getNumeroCuenta() {
		return numeroCuenta;
	}
	public void setNumeroCuenta(String numeroCuenta) {
		this.numeroCuenta = numeroCuenta;
	}
	public String getMonto() {
		return monto;
	}
	public void setMonto(String monto) {
		this.monto = monto;
	}
	public String getFolio_retencion() {
		return folio_retencion;
	}
	public void setFolio_retencion(String folio_retencion) {
		this.folio_retencion = folio_retencion;
	}

	
}
